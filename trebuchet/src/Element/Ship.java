package Element;


public class Ship {
	String name;
	long ID;
	int attack;
	int defined;
	int shield;
	int health;
	int hitPoint;
	int hedge;
	
	//List<Advance> advancelist
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public long getID() {
		return ID;
	}
	public void setID(long iD) {
		ID = iD;
	}
	public int getAttack() {
		return attack;
	}
	public void setAttack(int attack) {
		this.attack = attack;
	}
	public int getDefined() {
		return defined;
	}
	public void setDefined(int defined) {
		this.defined = defined;
	}
	public int getShield() {
		return shield;
	}
	public void setShield(int shield) {
		this.shield = shield;
	}
	public int getHealth() {
		return health;
	}
	public void setHealth(int health) {
		this.health = health;
	}
	public int getHitPoint() {
		return hitPoint;
	}
	public void setHitPoint(int hitPoint) {
		this.hitPoint = hitPoint;
	}
	public int getHedge() {
		return hedge;
	}
	public void setHedge(int hedge) {
		this.hedge = hedge;
	}
	
}
