package Element;

public class Resources {
	
	private int metal; //金属
	private int fule; //燃料
	private int feed; //食物
	private int power; //电力
	private int crystal; //补给
	
	public int getMetal() {
		return metal;
	}
	public void setMetal(int metal) {
		this.metal = metal;
	}
	public int getFule() {
		return fule;
	}
	public void setFule(int fule) {
		this.fule = fule;
	}
	public int getFeed() {
		return feed;
	}
	public void setFeed(int feed) {
		this.feed = feed;
	}
	public int getPower() {
		return power;
	}
	public void setPower(int power) {
		this.power = power;
	}
	public int getCrystal() {
		return crystal;
	}
	public void setCrystal(int crystal) {
		this.crystal = crystal;
	}
	
}
